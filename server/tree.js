// take the raw tree above and pad it out with extra info,
// such as IDs and filetypes, for easy access via the API.
const tree = (() => {

	let newTree = [];
	let idCounter = 1;

	function registerItem(item, parentId){

		item.id = "a"+(idCounter++);
		item.parentId = parentId;

		if(item.type == "FOLDER"){
			item.numberOfChildren = (item.children || []).length;
			item.children.forEach(function(childItem){
				registerItem(childItem, item.id);
			});
			delete item.children;
		}
		if(item.type == "FILE"){
			let fileTypeMatch = item.name.match(/\.([a-zA-Z]+)$/);
			item.fileType = (fileTypeMatch ? fileTypeMatch[1] : "jpg").toUpperCase();
		}

		newTree.push(item);

	};

	rawTree.forEach(function(item){
		registerItem(item, null);
	});

	return newTree;

})();

// wrap an item to be exported:
function wrap(item){
	item = copy(item);
	return item;
}

// copy just enough to get by:
function copy(item){
	if(Array.isArray(item)){
		let arr = [];
		for(let i = 0; i < item.length; i++){
			arr.push(copy(item[i]));
		}
		return arr;
	}
	else if(typeof item == "object" && item !== null){
		let obj = {};
		for(let j in item){
			obj[j] = copy(item[j]);
		}
		return obj;
	}
	else {
		return item;
	}
}

// get an item by ID:
function getById(id){
	let item;
	for(let i = 0; i < tree.length; i++){
		item = tree[i];
		if(item.id === id) return wrap(item);
	}
	return false;
}

// export a small internal API for fetching things from our tree:
module.exports = {
	get: getById,
	childIds: (id) => {
		if(id){
			let parent = getById(id);
			if(!parent || parent.type !== "FOLDER") return false;
		}
		return tree.reduce((arr, item) => {
			if(item.parentId === id) arr.push(item.id);
			return arr;
		}, []);
	},
	children: (id) => {
		if(id){
			let parent = getById(id);
			if(!parent || parent.type !== "FOLDER") return false;
		}
		return tree.reduce((arr, item) => {
			if(item.parentId === id) arr.push(wrap(item));
			return arr;
		}, []);
	}
};